package com.company;

public class Tipos {
    public static void main(String[] args) {

        //Números enteros//
        byte number1 = 5; //Capacidad almacenamiento 1 bit//
        short number2 = 3; //Capacidad almacenamiento 2 bits//
        int number3 = 10;  //Capacidad almacenamiento 4 bits//
        long number4 = 23; //Capacidad almacenamiento 8bits//

        //Punto flotante//
        float decimal1 = 10.5f; //colocar la f al final para saber que es un flotante//
        double decimal2 = 23.42d; //colocar la d al final para saber que es un double//

        //Character//
        char caracter1 = 'a'; //Está pensado para poner una sola letra. Si se ponen más, dará error//

        //Booleanos//
        boolean verdadero = true;
        boolean falso = false;

        //Cadenas de texto//
        String cadena1 = "Mi nombre es Sandra"; //El character va entre '' y el string entre ""//
        String cadena2 = "Mi apellido es León";

        //Tipos envoltorio//
        Integer number5 = null;
        Long number6 = 7L;




    }
}
